import random

# тут не зовсім зрозуміло, адже я уже його імпортував на початку файлу,
# мабуть він і так працює. Але для перестраховки імпортував ще раз.

user_input = input("Додайте текст: ")

# перетворюю введене на список
letters = list(user_input)

for _ in range(5):
    # міксую елементи списку методом .suffle (порада від GPT)
    random.shuffle(letters)
    # склеюю докупи перемішані символи
    shuffled_word = ''.join(letters)
    print(shuffled_word)
