# довелось використовувати подвійні лапки через апостроф в укр.мові
name = input("Напиши своє ім'я: ")

while True:
    age = input("Скільки тобі років? Напиши тут число: ")

    if not age.isdigit():  # перевірка чи введене значення числове
        print("Напиши свій вік числами!")
        continue  # повторення циклу поки не буде введене коректне значення

    age = int(age)
    print(f'Привіт, {name}! У твій наступний День народження тобі буде {age + 1} років!')
    break  # ззавершення циклу

