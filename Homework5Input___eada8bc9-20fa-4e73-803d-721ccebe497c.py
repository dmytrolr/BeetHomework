import random

print("""           ГРА 'ВГАДАЙ ЧИСЛО'           """)
user_name = input('Як тебе звати: ')  #
print(f'Привіт, {user_name}! Ти маєш вгадати число від 1 до 10.')
# застосовую цикл, ідея в тому, що користувач не має
# перезапускати гру вручну
while True:
    player = input("Додай обране число сюди. Щоб зупинити гру, "
                   "напиши СТОП: ").lower().strip()
    # обрізаю пробіли та переводжу в нижній регістр для виконання
    # наступної умови циклу:

    if player == 'стоп':
        print("Гра завершена. Дякую за участь!")
        break
    # перевірка типу введених даних
    if not player.isdigit():
        print('Можна вводити лише число!')
        continue
    # вивід обраного числа, для візуалізації
    player = int(player)
    print(f'Ти обрав число {player}!')
    # змінна laptop виводить випадкові числа у діапазоні 1-10
    laptop = random.randint(1, 10)

    if player != laptop:
        print(f'Ти програв! Загадане число: {laptop}, твоє число: {player}')
    else:
        print(f'Ти вгадав! Загадане число: {laptop}, твоє число: {player}')

